/**
 * A class representing the application URLs.
 * This class contains static readonly properties for different URLs used in the application.
 */
export default class ApplicationURL {
  // The base URL of the application.
  public static readonly BASE_URL = "https://www.demoblaze.com/";

  // The URL of the cart page.
  public static readonly CART_URL = this.BASE_URL + "cart.html";
}
